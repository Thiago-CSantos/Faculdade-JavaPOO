package com.mycompany.aula21_08.entity;

public class Soma {
    // Overloading
    public int somar(int x, int y) {
        return x + y;
    }
    
    public double somar(double x, double y) {
        return x + y;
    }

    public float somar(float x, float y) {
        return x + y;
    }
}
