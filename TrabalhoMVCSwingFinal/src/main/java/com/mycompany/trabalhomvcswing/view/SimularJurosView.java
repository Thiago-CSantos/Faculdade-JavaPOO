    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnDeletarActionPerformed

        // Solicita ao usuário que informe o ID que deseja deletar
        String id = JOptionPane.showInputDialog("Informe o ID que deseja deletar do seu Histórico");

        // Converte o ID fornecido em um valor inteiro
        Integer identificador = Integer.valueOf(id);

        // Obtém o modelo da tabela jTabelaJuros
        DefaultTableModel tabela = (DefaultTableModel) jTabelaJuros.getModel();

        // Loop através de todas as linhas da tabela
        for (int linha = 0; linha < tabela.getRowCount(); linha++) {
            // Obtém o valor da primeira coluna (coluna 0) da linha atual e converte para um
            // valor inteiro
            int aux = Integer.valueOf(tabela.getValueAt(linha, 0).toString());

            // Verifica se o ID fornecido é igual ao ID da linha atual
            if (identificador == aux) {
                // Remove a linha da tabela onde o ID corresponde
                tabela.removeRow(linha);
                System.out.println("Deletado: " + linha); // Exibe uma mensagem informando que a linha foi deletada
            }
        }

    }// GEN-LAST:event_btnDeletarActionPerformed
