    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnCalcularActionPerformed
        // Extrai o valor do campo de texto txtMontante e o converte para um valor
        // double
        double montante = Double.parseDouble(txtMontante.getText());

        // Extrai o valor do campo de texto txtTaxaJurosAnos e o converte para um valor
        // double
        double taxaJurosAnos = Double.parseDouble(txtTaxaJurosAnos.getText());

        // Extrai o valor do campo de texto txtPeriodoAnos e o converte para um valor
        // inteiro
        int periodoAnos = Integer.parseInt(txtPeriodoAnos.getText());

        // Cria uma instância da classe JurosComposto com os valores obtidos dos campos
        // de texto
        JurosComposto juros = new JurosComposto(montante, taxaJurosAnos, periodoAnos);

        // Calcula os juros compostos usando a instância juros
        double resultado = juros.calcularJurosCompostos();

        // Cria um objeto DecimalFormat para formatar o resultado com duas casas
        // decimais
        DecimalFormat df = new DecimalFormat("#.##");

        // Obtém o modelo da tabela jTabelaJuros
        DefaultTableModel tabela = (DefaultTableModel) jTabelaJuros.getModel();

        // Adiciona uma nova linha à tabela com os valores calculados
        tabela.addRow(new Object[] { idJuros, montante, taxaJurosAnos, periodoAnos, df.format(resultado) });

        // Incrementa o idJuros para a próxima entrada
        idJuros++;

    }// GEN-LAST:event_btnCalcularActionPerformed

